/**
 * op_add - Returns sum
 * @a: integer
 * @b: integer
 *
 * Return:integer
 */
int op_add(int a, int b)
{
    return (a + b);
}

/**
 * op_sub - Returns the difference
 * @a: integer
 * @b:  integer
 *
 * Return: integer
 */
int op_sub(int a, int b)
{
    return (a - b);
}

/**
 * op_mul - Returns the product
 * @a: integer
 * @b: integer
 *
 * Return: integer
 */
int op_mul(int a, int b)
{
    return (a * b);
}

/**
 * op_div - Returns the result of the di
 * @a: integer
 * @b:  integer
 *
 * Return : integer
 */
int op_div(int a, int b)
{
    return (a / b);
}

/**
 * op_mod - Returns the remainder of the division of two numbers
 * @a:  integer
 * @b: integer
 *
 * Return: integer
 */
int op_mod(int a, int b)
{
    return (a % b);
}
